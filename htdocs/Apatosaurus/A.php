    <h1>Apatosaurus</h1>
    <p>Apatosaurus is a genus of herbivorous sauropod dinosaur that lived in North America during the
        Late Jurassic period. Othniel Charles Marsh described and named the first-known species, A. ajax,
        in 1877, and a second species, A. louisae, was discovered and named by William H. Holland in 1916.
        Apatosaurus lived about 152 to 151 million years ago (mya), during the late Kimmeridgian to early
        Tithonian age, and are now known from fossils in the Morrison Formation of modern-day Colorado, Oklahoma,
        New Mexico, Wyoming, and Utah in the United States. Apatosaurus had an average length of 21–23 m (69–75 ft),
        and an average mass of 16.4–22.4 t (16.1–22.0 long tons; 18.1–24.7 short tons). A few specimens indicate a
        maximum length of 11–30% greater than average and a mass of approximately 33 t (32 long tons; 36 short tons).
    </p>
